import Ember from 'ember';

export default Ember.Component.extend({
  id: 'filter',
  active: false,
  classNames: ['dropdown'],
  classNameBindings: ['active'],
  click: function() {
    this.set('active', !this.get('active'));
    return false;
  },
  actions: {
    selectJurisdiction: function(jurisdiction) {
      this.sendAction('action', jurisdiction);
    }
  }
});
